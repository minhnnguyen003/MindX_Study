// get data 

export const getFullData = () => {
    return JSON.parse(localStorage.getItem("listTask"));
}

// get data with day

export const getDataDay = (dateTime) => {
    const listFullData = getFullData();
    const listDataDay = {};
    for(let id in listFullData) {
        if(listFullData[id].dateTime == dateTime) {
            listDataDay[id] = listFullData[id]; 
        }
    }
    return JSON.stringify(listDataDay);
}

// get data with filter 

export const getDataFilter = (key) => {
    const listFullData = getFullData();
    const listDataFilter = {};
    for(let id in listFullData) {
        if(listFullData[id].value.includes(removeTheSign(key))) {
            listDataFilter[id] = listFullData[id];
        }
    }
    return JSON.stringify(listDataFilter);
}

// get date

export const getDate = () => {
    const time = new Date();
    return `${time.getDate()}/${time.getMonth() + 1}/${time.getFullYear()}`;
}

// get time

export const getTime = () => {
    const time = new Date();
    return [time.getHours(), time.getMinutes() , time.getSeconds()];
}

// set id for task

export const setTaskID = () => {
    const time = new Date();
    return time.getTime();
}

// support filter - remove the sign
export const removeTheSign = (str) => {
    str = str.replace(/à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ/g, "a");
    str = str.replace(/è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ/g, "e");
    str = str.replace(/ì|í|ị|ỉ|ĩ/g, "i");
    str = str.replace(/ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ/g, "o");
    str = str.replace(/ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ/g, "u");
    str = str.replace(/ỳ|ý|ỵ|ỷ|ỹ/g, "y");
    str = str.replace(/đ/g, "d");
    str = str.replace(/À|Á|Ạ|Ả|Ã|Â|Ầ|Ấ|Ậ|Ẩ|Ẫ|Ă|Ằ|Ắ|Ặ|Ẳ|Ẵ/g, "A");
    str = str.replace(/È|É|Ẹ|Ẻ|Ẽ|Ê|Ề|Ế|Ệ|Ể|Ễ/g, "E");
    str = str.replace(/Ì|Í|Ị|Ỉ|Ĩ/g, "I");
    str = str.replace(/Ò|Ó|Ọ|Ỏ|Õ|Ô|Ồ|Ố|Ộ|Ổ|Ỗ|Ơ|Ờ|Ớ|Ợ|Ở|Ỡ/g, "O");
    str = str.replace(/Ù|Ú|Ụ|Ủ|Ũ|Ư|Ừ|Ứ|Ự|Ử|Ữ/g, "U");
    str = str.replace(/Ỳ|Ý|Ỵ|Ỷ|Ỹ/g, "Y");
    str = str.replace(/Đ/g, "D");
    return str;
}